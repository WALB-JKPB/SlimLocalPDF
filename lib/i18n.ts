export function getCopy(locale: Locale): AppCopy {
  if (locale === 'ko') return copy.ko;
  if (locale === 'en') return copy.en;
  return { ...copy.en, ...translated[locale] };
}

export function formatText(template: string, values: Record<string, string | number>) {
  return Object.entries(values).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, String(value)),
    template,
  );
}
